package com.example.RootExtracterApp.model;

public class WordRoots {
	public String word;
	public String root;
	public WordRoots(String word, String root) {
		super();
		this.word = word;
		this.root = root;
	}
	public String getWord() {
		return word;
	}
	public String getRoot() {
		return root;
	}
	
	
	

}
